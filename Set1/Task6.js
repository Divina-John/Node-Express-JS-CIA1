let students = [
    { name: "John", marks: 85 },
    { name: "Paul", marks: 92 },
    { name: "Richard", marks: 70 },
    { name: "George", marks: 88 }
];
for (let student of students) {
    console.log("Name:", student.name, "Marks:", student.marks);
}
/*
Output:
Name: John Marks: 85
Name: Paul Marks: 92
Name: Richard Marks: 70
Name: George Marks: 88
*/
